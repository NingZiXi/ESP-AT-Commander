/** 根据分类 id 获取对应图标 */
export function getCategoryIcon(catId: string, size = 14) {
  const key = catId.toLowerCase();
  if (key.includes("wifi") || key.includes("wi-fi")) return <Wifi size={size} />;
  if (key.includes("ble") || key.includes("bluetooth")) return <Bluetooth size={size} />;
  if (key.includes("mqtt")) return <Radio size={size} />;
  if (key.includes("http") || key.includes("tcp")) return <Globe size={size} />;
  if (key.includes("basic") || key.includes("system")) return <Cpu size={size} />;
  return <Folder size={size} />;
}
